export function readLocale() {
  try {
    const prefs = JSON.parse(localStorage.getItem(PREFS_KEY) || "{}")
    const lang = prefs.language || "zh-CN"
    return lang === "en" ? "en" : "zh-CN"
  } catch {
    return "zh-CN"
  }
}

export function writeLocale(locale) {
  try {
    const prefs = JSON.parse(localStorage.getItem(PREFS_KEY) || "{}")
    prefs.language = locale === "en" ? "en" : "zh-CN"
    localStorage.setItem(PREFS_KEY, JSON.stringify(prefs))
    document.documentElement.lang = prefs.language === "en" ? "en" : "zh-CN"
    window.dispatchEvent(new CustomEvent("canvas-prefs-changed"))
  } catch {
    /* ignore */
  }
}

export function translate(locale, key, vars = {}) {
  const table = MESSAGES[locale] || MESSAGES["zh-CN"]
  let text = table[key] ?? MESSAGES["zh-CN"][key] ?? key
  Object.entries(vars).forEach(([k, v]) => {
    text = text.replace(`{${k}}`, String(v))
  })
  return text
}

export function useLocale() {
  const [locale, setLocale] = useState(readLocale)

  useEffect(() => {
    document.documentElement.lang = locale === "en" ? "en" : "zh-CN"
    const onChange = () => setLocale(readLocale())
    window.addEventListener("canvas-prefs-changed", onChange)
    return () => window.removeEventListener("canvas-prefs-changed", onChange)
  }, [locale])

  const t = useCallback((key, vars) => translate(locale, key, vars), [locale])

  const setLanguage = useCallback((lang) => {
    writeLocale(lang)
    setLocale(readLocale())
  }, [])

  return { locale, t, setLanguage }
}

/** Non-React callers (hooks, utils) */
export function getT() {
  const locale = readLocale()
  return (key, vars) => translate(locale, key, vars)
}
